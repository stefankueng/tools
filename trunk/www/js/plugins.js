$(document).ready(function() {
    "use strict";
    $(".fancybox").fancybox({});

    $(window).scroll(function() {
        if ($(this).scrollTop() > 100) {
            $('.scrollup').fadeIn();
        } else {
            $('.scrollup').fadeOut();
        }
    });

    $('.scrollup').click(function() {
        $('html, body').animate({ scrollTop: 0 }, 'normal'); /*slow, normal, fast, 600 (ms)*/
        return false;
    });

});

